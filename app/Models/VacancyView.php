<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class VacancyView extends Model
{
    protected $fillable = [
        'user_id',
        'vacancy_id',
        'viewed_at',
    ];

    public function user()
    {
        return $this->belongsTo(User::class);
    }

    public function vacancy()
    {
        return $this->belongsTo(Vacancy::class);
    }
}
